    public void leerClientes() {
        String ubicacion = "DatosClientes.csv";
        File archivo = new File(ubicacion);
        try ( Scanner sc = new Scanner(archivo)) {
            while (sc.hasNextLine()) {
                String linea = sc.nextLine();
                String[] datos = linea.split(";");
                Cliente e = new Cliente();
                e.setDpi(datos[0]);
                e.setNombre(datos[1]);
                e.setTelefono(datos[2]);
                e.setDireccion(datos[3]);
            }
            System.out.println("Clientes cargados exitosamente");
        } catch (IOException ex) {
            System.out.println("Ocurrio un error al tratar de leer el archivo");
        }
    }
    
    
    public void leerCuenta() {
        String ubicacion = "DatosCuentas.csv";
        File archivo = new File(ubicacion);
        try ( Scanner sc = new Scanner(archivo)) {
            while (sc.hasNextLine()) {
                String linea = sc.nextLine();
                String[] datos = linea.split(";");
                Cuenta r = new Cuenta();
                r.setNumerocuenta(datos[0]);
                r.setTipocuenta(datos[1]);
                r.setTipomoneda(datos[2]);
            }
            System.out.println("Cuentas cargadas exitosamente");
        } catch (IOException ex) {
            System.out.println("Ocurrio un error al tratar de leer el archivo");
        }
    }
    
   
